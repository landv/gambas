int COMMON_strcasecmp(const char *s1, const char *s2)
{
  register offset_t i;
  register int d;
  register char c;
  
  for (i = 0;; i++)
  {
    c = COMMON_tolower[(unsigned char)s1[i]];
    d = c - COMMON_tolower[(unsigned char)s2[i]];
    if (d < 0)
      return -1;
    else if (d > 0)
      return 1;
    else if (c == 0)
      return 0;
  }
}

int COMMON_strncasecmp(const char *s1, const char *s2, size_t n)
{
  register offset_t i;
  register int d;
  register char c;
  
  for (i = 0; i < n; i++)
  {
    c = COMMON_tolower[(unsigned char)s1[i]];
    d = c - COMMON_tolower[(unsigned char)s2[i]];
    if (d < 0)
      return -1;
    else if (d > 0)
      return 1;
  }
  
  return 0;
}


